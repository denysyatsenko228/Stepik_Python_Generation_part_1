def is_password_good(password):
    if len(password) < 8:
        return False

    upper_count = 0
    lower_count = 0
    digit_count = 0

    for char in password:
        if char.isupper():
            upper_count += 1
        elif char.islower():
            lower_count += 1
        elif char.isdigit():
            digit_count += 1

    return upper_count > 0 and lower_count > 0 and digit_count > 0


